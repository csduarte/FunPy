# A List of Escape Sequences

# \\ - Backslash
# \' - Single quote
# \" - Double Quote
# \a - ASCII bell(BEL)
# \b - ASCII backspace
# \f - ASCII formfeed
# \n - ASCII linefeed
# \N{name}  - Character named name in the Unicode database (unicode only)
# \r - ASCII carriage return
# \t - ASCII horizontal tab
# \uxxxx - 16-bit hex value xxxx
# \Uxxxxxxxx - 32-bit hex value
# \v - ASCII vertical tab
# \ooo - Character with octal value 00
# \xhh